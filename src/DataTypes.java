public class DataTypes {
    public static void main(String[] args) {
        Boolean one = false;
        byte a = 10;
        short b = 10000;
        int c = 100000;
        long d = 10000L;
        float e = 234.5f;
        double f = 13.2;
        char g = 'g';
    }
}

